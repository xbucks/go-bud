func Generator(dir string, fsCache *fscache.Cache, modCache *modcache.Cache) (*budfs.FileSystem, error) {
	module, err := gomod.Find(dir, gomod.WithFSCache(fsCache), gomod.WithModCache(modCache))
	if err != nil {
		return nil, err
	}
	bfs, err := budfs.Load(module, budfs.WithFSCache(fsCache))
	if err != nil {
		return nil, err
	}
	generatorMap := Load(bfs, module)
	for path, generator := range generatorMap {
		bfs.Entry(path, generator)
	}
	return bfs, nil
}

// Generated
func load(
	action *action,
	web *web,
) Map {
	return Map{
		"bud/action/action.go": gen.FileGenerator(action),
		"bud/web/web.go":       gen.FileGenerator(web),
	}
}

type Map map[string]gen.Generator

// Generated by DI
func Load(budfsFS budfs.FS, modModule *gomod.Module) Map {
	parserParser := parser.New(budfsFS, modModule)
	action := &action{budfsFS, parserParser}
	web := &web{budfsFS}
	return load(action, web)
}
